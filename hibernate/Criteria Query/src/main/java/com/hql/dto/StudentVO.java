package com.hql.dto;

import javax.persistence.Column;

public class StudentVO {

	private String studentName;

	private Double studentMarks;

	private Integer studentRank;

	public StudentVO(String studentName, Double studentMarks, Integer studentRank) {
		super();
		this.studentName = studentName;
		this.studentMarks = studentMarks;
		this.studentRank = studentRank;
	}

	public String getStudentName() {
		return studentName;
	}

	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}

	public Double getStudentMarks() {
		return studentMarks;
	}

	public void setStudentMarks(Double studentMarks) {
		this.studentMarks = studentMarks;
	}

	public Integer getStudentRank() {
		return studentRank;
	}

	public void setStudentRank(Integer studentRank) {
		this.studentRank = studentRank;
	}

	@Override
	public String toString() {
		return "StudentVO [studentName=" + studentName + ", studentMarks=" + studentMarks + ", studentRank="
				+ studentRank + "]";
	}

}
